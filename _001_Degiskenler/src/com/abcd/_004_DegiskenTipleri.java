		System.out.println("Byte tipi     : " + myByte2);
		System.out.println("Short tipi    : " + myShort2);
		System.out.println("Long tipi     : " + myLong2);
		System.out.println("Float tipi    : " + myFloat2);
		System.out.println("Double tipi   : " + myDouble2);
		System.out.println("Boolean tipi  : " + myBoolean2);
		System.out.println("Character tipi: " + myChar2);
		System.out.println("String tipi   : " + myString);
		
		
		System.out.println("------------------------");
		System.out.println("Integer tipi min  : " + myInt2.MIN_VALUE);
		System.out.println("Integer tipi max  : " + myInt2.MAX_VALUE);

		System.out.println("Byte tipi min     : " + myByte2.MIN_VALUE);
		System.out.println("Byte tipi max     : " + myByte2.MAX_VALUE);

